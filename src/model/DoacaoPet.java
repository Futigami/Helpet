package model;

public class DoacaoPet {
	private String animal;
	private int idadeAnimal;
	private String racaAnimal;
	private String sexoAnimal;
	private String estadoAnimal;
	private String cidadeAnimal;
	
	
	//GET e SET
	public String getAnimal() {
		return animal;
	}
	public void setAnimal(String animal) {
		this.animal = animal;
	}
	public int getIdadeAnimal() {
		return idadeAnimal;
	}
	public void setIdadeAnimal(int idadeAnimal) {
		this.idadeAnimal = idadeAnimal;
	}
	public String getRacaAnimal() {
		return racaAnimal;
	}
	public void setRacaAnimal(String racaAnimal) {
		this.racaAnimal = racaAnimal;
	}
	public String getSexoAnimal() {
		return sexoAnimal;
	}
	public void setSexoAnimal(String sexoAnimal) {
		this.sexoAnimal = sexoAnimal;
	}
	public String getEstadoAnimal() {
		return estadoAnimal;
	}
	public void setEstadoAnimal(String estadoAnimal) {
		this.estadoAnimal = estadoAnimal;
	}
	public String getCidadeAnimal() {
		return cidadeAnimal;
	}
	public void setCidadeAnimal(String cidadeAnimal) {
		this.cidadeAnimal = cidadeAnimal;
	}

	
}
